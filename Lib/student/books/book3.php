                            Young Nadia Owusu followed her father, a United Nations official, from Europe to Africa and back again. Just as she and her family settled into a new home, her father would tell them it was time to say their goodbyes. The instability wrought by Nadia’s nomadic childhood was deepened by family secrets and fractures, both lived and inherited. Her Armenian American mother, who abandoned Nadia when she was two, would periodically reappear, only to vanish again. Her father, a Ghanaian, the great hero of her life, died when she was thirteen. After his passing, Nadia’s stepmother weighed her down with a revelation that was either a bombshell secret or a lie, rife with shaming innuendo.<br><br>
                            With these and other ruptures, Nadia arrived in New York as a young woman feeling stateless, motherless, and uncertain about her future, yet eager to find her own identity. What followed, however, were periods of depression in which she struggled to hold herself and her siblings together. <br><br>
                            “A magnificent, complex assessment of selfhood and why it matters” (Elle), Aftershocks depicts the way she hauled herself from the wreckage of her life’s perpetual quaking, the means by which she has finally come to understand that the only ground firm enough to count on is the one written into existence by her own hand.<br><br>
